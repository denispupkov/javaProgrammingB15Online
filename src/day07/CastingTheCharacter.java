package day07;

public class CastingTheCharacter {

    public static void main(String[] args) {

        char grade = 'B' ;
        System.out.println(grade);

        int letterInNumber = 'c';

        System.out.println(letterInNumber);

        int letterNumber2 = 'b';

        System.out.println(letterNumber2);

        char myFirstChar = 100;
        System.out.println(myFirstChar);


        int d = 'd';
        int e = 'e';
        int n = 'n';
        int i = 'i';
        int s = 's';

        System.out.println("d is "+d+","+"e is "+e+','+"n is "+n+","+"i is "+i+","+"s is "+s); //make it as a string
        System.out.println(d+e+n+i+s); // make it as a int
    }
}
