package day04;

public class convertingTemperatures {
    public static void main(String[] args) {


        double number1 = 5.0;
        int number2 = 9;
        double fahrenheit =65;

        double celsius = (number1/number2)*(fahrenheit-32);

        System.out.println(fahrenheit+"F"+" equals to "+celsius+"C");

    }
}
