package day21;

public class NestedLoopRectangular {
    public static void main(String[] args) {


        for (int i = 1; i <= 10; i++) {
            System.out.println();


            for (int x = 1; x < i; x++) {
                System.out.print(x);
            }
            System.out.print(i);
        }

        // System.out.println();


    }
}
